    private void buttonCajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCajaActionPerformed
        Persona person = CONTROL.getColaDeCaja().remove();
        person.getTiempos().setMomentoDeRecepcionEnEspera(new TimeSimul(new GregorianCalendar()));
        CONTROL.getColaDeAtencion().offer(person);
        Notifier.getNotificador().notificar("Update");
        Notifier.getNotificador().notificar("UPDATE_VIEW_QUEUE");
        buttonCaja.setEnabled(!CONTROL.getColaDeCaja().isEmpty());
        buttoSalida.setEnabled(!CONTROL.getColaDeAtencion().isEmpty());
    }//GEN-LAST:event_buttonCajaActionPerformed

