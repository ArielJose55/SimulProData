    private final class ButtonListener implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
            Persona persona = new Persona();
            String[] values = e.getActionCommand().split("-");
            persona.setSexo(values[1]);
            persona.setTipo(values[0]);
            CONTROL.getPersonasProcesadas().add(persona);
            CONTROL.getColaDeCaja().offer(persona);
            Notifier.getNotificador().notificar("Update");
            Notifier.getNotificador().notificar("UPDATE_VIEW_QUEUE");
            buttonCaja.setEnabled(!CONTROL.getColaDeCaja().isEmpty());
        }
        
    }
    

